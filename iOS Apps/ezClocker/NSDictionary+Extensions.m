#import "NSDictionary+Extensions.h"

@implementation NSDictionary (NSDictionaryExtensions)

+ (BOOL)isNilOrNull:(NSDictionary *)dict {
    if (nil == dict || dict == (NSDictionary*)[NSNull null]) {
        return TRUE;
    }
    return FALSE;
}

@end
